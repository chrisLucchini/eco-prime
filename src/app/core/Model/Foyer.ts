export class Foyer {

  prime: number;
  intitulePrime: string;
  nbPersonne: number;
  nbItem: number;
  revenus: number;
  nomPrime: string;

  constructor(prime, intitulePrime, nbPersonne, nbItem, revenus, nomPrime) {
    this.prime = prime;
    this.intitulePrime = intitulePrime;
    this.nbPersonne = nbPersonne;
    this.nbItem = nbItem;
    this.revenus = revenus;
    this.nomPrime = nomPrime;
  }
}
